        DecimalFormat df=new DecimalFormat("#.####");
        ArrayList aspectorientationFinderResults=OrientationFinder.aspectorientationFinderResults;
            System.out.println("*************************************4********************************************");
            System.out.println("Final Result");
            System.out.println("**********************************************************************************");
            System.out.println("Aspect\tPositiveScore\tNegativeScore\tAVScore\tSTDScore");
            String ro="";
            for(int i=0;i<aspectorientationFinderResults.size();i++)
            {
                String sk=aspectorientationFinderResults.get(i).toString().trim();
                String s[]=sk.trim().split("\t");
                double positivescore=Double.parseDouble(s[1].trim());
                double negativescore=Double.parseDouble(s[2].trim());
                double AVScore=(double)(positivescore+negativescore)/(double)2;
                double a=(double)(positivescore-AVScore)*(double)(positivescore-AVScore);
                double b=(double)(negativescore-AVScore)*(double)(negativescore-AVScore);
                double STDScore=Math.sqrt((double)((double)(a+b)/(double)2));
                System.out.println(sk.trim()+"\t"+df.format(AVScore)+"\t"+df.format(STDScore));
                ro=ro+sk.trim()+"\t"+df.format(AVScore)+"\t"+df.format(STDScore)+"\n";
            }
            System.out.println("*************************************4********************************************");
       
            String header="**********************************************************************************\n                 Final Result\n**********************************************************************************\nAspect\tPositiveScore\tNegativeScore\tAVScore\tSTDScore\n";
            String footer="**********************************************************************************";
